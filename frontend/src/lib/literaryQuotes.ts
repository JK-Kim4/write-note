/**
 * 목록에서 무작위 1개 선택. [rand] 주입으로 테스트에서 결정성 확보(기본 Math.random).
 * 빈 목록이면 null — 호출 측에서 기본 문구로 폴백.
 */
export function pickRandom(
    quotes: ReadonlyArray<LiteraryQuote>,
    rand: () => number = Math.random,
): LiteraryQuote | null {
    if (quotes.length === 0) return null;
    const index = Math.floor(rand() * quotes.length);
    return quotes[Math.min(index, quotes.length - 1)] ?? null;
}
